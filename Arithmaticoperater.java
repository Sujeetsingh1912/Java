public class Arithmaticoperater {
   public static void main(String[] args) {
      
       int a = 0b1010;
       int b = 05;
       System.out.println("ADD    : "+(a+b));
       System.out.println("SUB    : "+(a-b));
       System.out.println("MUL    : "+(a*b));
       System.out.println("DIV    : "+(a/b));
        
       int a1 = 10;
       int b1 = 3;
      
       System.out.println(a1+b1);
       System.out.println(a1-b1);
       System.out.println(a1*b1);
       System.out.println(a1/b1);
       System.out.println(a1%b1);


   }   
}







