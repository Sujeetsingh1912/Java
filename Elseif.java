public class Elseif {
   public static void main(String[] args) {
     
     int sum1 = 30 ;
     int sum2 = 20 ;
    if (sum1 > sum2){
        System.out.println(sum1+"    is the biggest no.");
    }
    else if (sum1 < sum2){
        System.out.println(sum2+"   is the biggest no.");
    }
    else {
        System.out.println(sum1+ "," +sum2+ "   is the Equal");
    } 

   }
}