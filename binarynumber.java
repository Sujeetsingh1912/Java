public class binarynumber
{
        public static void main(String[] args)
    {
        int a = 10;
        System.out.println(a);
        int b = 0b10;
        System.out.println(b);
        int c = 0b1010;
        System.out.println(c);
        int d = 0B1111;
        System.out.println(d); 
      

    }
}
