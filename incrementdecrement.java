public class incrementdecrement
{
 public static void main(String[] args) 
   {
      
       int a = 5;
       System.out.println(++a);
      
       int b = 5;
       System.out.println(b++);


       int c = 5;
       System.out.println(--c);


       int d = 5;
       System.out.println(d--);


   }   
}
